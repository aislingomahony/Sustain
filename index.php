	<p>Trinity College Dublin is delighted to host ‘Sustain: Endophytes for a Growing World’, an international conference taking place from 28th to 29th August 2017 in the historic setting of Ireland’s oldest and most prestigious university.</p>


	<p>The two-day conference will be centred on the core themes of manipulating the plant microbiome for sustainable plant production in a changing world. Climate change will present formidable problems for many areas of plant production, from agriculture to forestry and horticulture. Understanding the potential beneficial role of endophytes in helping plants to cope with the associated stresses will be a crucial factor in future sustainable plant production practices. The conference will highlight cutting-edge research using endophytes and will synthesise the most promising approaches to endophyte supported plant production.
	</p>
	 
	<p class="align-center">
		<a href="mailto:sustain2017@tcd.ie?subject=Expression of interest in attending Sustain 2017">Register your interest in attending</a>
	</p>
	 

	<p>The conference will be based around four themed sessions, each focusing on a particular aspect of plant-endophyte interactions featuring a keynote talk from an invited speaker:
	<ol class="list">
	<li class="list">Phenotyping the endophyte symbiosis</li>
	<li class="list">Molecular tools to determine endophyte mode of action</li>
	<li class="list">The plant holobiont as a single target system</li>
	<li class="list">Tailoring endophyte inoculants for successful applications</li></ol>
	</p>
	<br/>

	<p>
	<a>We invite you to present your work on endophytes as an oral presentation or as a poster for one of the themed sessions.</a>
	</p>

	<p>Presented papers will be considered for publication in an associated edited book and a special journal issue of ‘Plant Ecology and Diversity’.</p>


	<p>On behalf of the organizing committee we invite you to participate in the Sustain conference, and look forward to seeing you in Dublin.</p>

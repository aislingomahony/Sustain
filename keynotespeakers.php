      <h1>Keynote Speakers</h1>
        <h2>Prof. David B. Collinge</h2>
        <p><img src="../assets/img/Sustain_Conference_2017/keynote speakers/David B Collinge.jpg" alt="collinge-d-b" width="231" height="237" class="left">David B. Collinge is professor of plant pathology at the  University of Copenhagen. He received his Ph.D. in genetics at the University  of Newcastle upon Tyne, UK, in 1982, and worked subsequently at Aarhus  University, Denmark and The John Innes Institute, Norwich, UK before moving to  Copenhagen in 1987. Prior the present position he was associate professor at  the then Royal Veterinary and Agricultural University. <br>
      His research involves two main themes: (1) defence  mechanisms in plants (2) the study of endophytes and their ability to  manipulate plants. Both these topics relate to a desire to improve the  sustainability of agriculture through the use of biotechnology either using  transgenic plants or microorganisms for biocontrol.</p>
      <p>&nbsp;</p>
        <h2>Prof. Kari Saikkonen</h2>
        <p><img src="../assets/img/Sustain_Conference_2017/keynote speakers/Saikkonen photo.jpg" alt="saikkonen-kari" width="226" height="297" class="left">Kari Saikkonen is a professor in Natural Resources Institute  Finland (Luke). He received his Ph.D. in biology at the University of Turku,  Finland, in 1994, and then worked as a postdoctoral student with Prof. Stan  Faeth at Arizona State University in USA in 1995-1997. Prior the present  position he was an Academy Research Fellow (Academy of Finland), a research  professor of ecological plant production at MTT Agrifood Research Finland, and  a station manager of Kevo Subarctic Research Institute. <br>
      His research involves three main themes: (1) climate  change-driven species&rsquo; range shifts, (2) ecological and evolutionary  implications of genetic, species and functional diversity of plants and  associated micro-fungi and herbivores, and (3) new solutions for sustainable  agricultural practices to circumvent the heavy use of fertilizers and  pesticides.</p>
      <p>&nbsp;</p>
        <h2>Dr. Angela Sessitsch </h2>
        <p><img src="../assets/img/Sustain_Conference_2017/keynote speakers/Bild Sessitsch.jpg" alt="sessitsch-angela" width="285" height="190" class="left">Dr. Angela Sessitsch heads the Bioresources Unit of the AIT  Austrian Institute of Technology. She studied biochemistry at the University of  Technology in Graz, holds a PhD in Microbiology from the Wageningen University,  the Netherlands, and is habilitated at the Vienna University of Natural  Resources and Life Sciences. She has pioneered plant-associated microbiomes,  particularly in the endosphere, and she is interested in understanding the  interactions between plants, microbiomes and the environment as well as to  develop applications. Her group explores the diversity and functioning of plant  microbiota by applying a range of molecular approaches, interaction modes  between plants and model bacteria, colonization behaviour of endophytes as well  as various application technologies for biocontrol and crop enhancement  applications. Together with her group A. Sessitsch published more than 145  peer-reviewed publications.</p>
      <p>&nbsp;</p>
        <h2>Prof. Kornelia Smalla</h2>
        <p><img src="../assets/img/Sustain_Conference_2017/keynote speakers/Smallakornelia.jpg" alt="smalla-kornelia" width="221" height="332" class="left">Director and Professor; Institute for Epidemiology and  Pathogen Diagnostics; Julius KÃ¼hn Institut Braunschweig; Professor at the  Technical University Braunschweig.<br>
          Kornelia Smalla studied chemistry at the Martin Luther  University in Halle. She did her PhD in biochemistry and her habilitation in  microbiology at the Technical University Braunschweig. Her long term research  interest is unraveling the complex interaction in the rhizosphere and how soil  bacteria respond to changing environmental conditions. A particular focus of  her research is how agricultural management systems influence the structural  and functional diversity of soil- and plant-associated microbial communities,  the abundance plant and human pathogen abundance as well as plant beneficial  bacteria. A more recent research interest is on the understanding which factors  influence the presence transferable antibiotic resistance genes on fresh  produce. Kornelia Smalla received the honory doctorate in Agriculture  of the Swedish Agricultural University in 2011. Her h-index is 55 (Web of Science).

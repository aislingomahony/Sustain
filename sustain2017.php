      <p>Trinity  College Dublin is delighted to host <strong>&lsquo;Sustain: Endophytes for a Growing World&rsquo;</strong>,  an international conference taking place from 28th to 29th August 2017 in the  historic setting of Ireland&rsquo;s oldest and most prestigious university.</p>
        <p><br>
        The  two-day conference will be centred on the core themes of manipulating the plant  microbiome for sustainable plant production in a changing world. Climate change  will present formidable problems for many areas of plant production, from  agriculture to forestry and horticulture. Understanding the potential  beneficial role of endophytes in helping plants to cope with the associated  stresses will be a crucial factor in future sustainable plant production  practices. The conference will highlight cutting-edge research using endophytes  and will synthesise the most promising approaches to endophyte supported plant  production.</p>
        <p>&nbsp;</p>
        <h4><a href="mailto:Sustain2017@tcd.ie">Register your interest in attending</a></h4>
        <p>&nbsp;</p>
        <p>The  conference will be based around four themed sessions, each focusing on a  particular aspect of plant-endophyte interactions featuring a keynote talk from  an invited speaker:</p>
        <ol start="1" type="1">
          <li>Phenotyping the endophyte       symbiosis</li>
          <li>Molecular tools to       determine endophyte mode of action</li>
          <li>The plant holobiont as a       single target system</li>
          <li>Tailoring endophyte       inoculants for successful applications</li>
        </ol>
